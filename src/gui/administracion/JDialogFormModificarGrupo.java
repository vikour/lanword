    private void jButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActualizarActionPerformed
        boolean nombreActualizado = ((JTextFieldUpdate) jTextFieldNombre).isChangedValue();
        boolean descripcionActualizado = ((JTextAreaUpdate) jTextAreaDescripcion).isChangedValue();
        
        try {
            
            if (nombreActualizado) {
                bd.grupos.actualizarNombre(jTextFieldNombre.getText(), seleccionado.getNombre());
                seleccionado.setNombre(jTextFieldNombre.getText());
            }
            
            if (descripcionActualizado) {
                seleccionado.setDescripcion(jTextAreaDescripcion.getText());
                bd.grupos.actualizarDescripcion(seleccionado);
            }
            
            dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
        }
        catch (IllegalArgumentException ex) {
            tip.setMessage(ex.getMessage());
            tip.setVisible(true);
        }
        catch (SQLException ex) {
            tip.setMessage("Ya existe un grupo con ese nombre");            
            tip.setVisible(true);
        }
        
    }//GEN-LAST:event_jButtonActualizarActionPerformed

