    private void jButtonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearActionPerformed
        String nombre = jTextFieldNombre.getText();
        Idioma idioma = (Idioma) jComboBoxIdioma.getSelectedItem();
        Palabra palabra;
        int confirmarTraducir = 0;
        
        try {
            palabra = new Palabra(nombre, idioma);
            BDResolver.getInstance().palabras.anyadir(palabra);
            
            // Limpiar los datos, para la siguiente palabra.
            jTextFieldNombre.setText("");
            
            // Si ha seleccionado "traducir", se invoca a la pantalla de traducir.
            
            if (jCheckBox1.isSelected()) {
                this.setVisible(false);
                PalabrasCtrl.getInstance().setSeleccionada(palabra);
                
                try {
                    new JDialogFormTraducciones((Frame) SwingUtilities.getWindowAncestor(this), true).setVisible(true);
                    jCheckBox1.setSelected(false);
                }
                catch (Exception ex) {
                    PalabrasCtrl.getInstance().setSeleccionada(null);
                    JOptionPane.showMessageDialog(this, "Tienes que crear al menos 2 idiomas.", "Error", JOptionPane.WARNING_MESSAGE);
                }
                this.setVisible(true);
            }
            else if (!tip.isShowing()){
                tip.setComponentLinked(jTextFieldNombre);
                tip.setMessage("Palabra creada");
                tip.setMode(Tooltip.SUCCESS);
                tip.setVisible(true);
            }
 
        }
        catch (IllegalArgumentException ex) {
            
            if (!tip.isShowing()) {
                tip.setComponentLinked(jTextFieldNombre);
                tip.setMessage(ex.getMessage());
                tip.setMode(Tooltip.ERROR);
                tip.setVisible(true);
            }

        }
        catch (SQLException ex) {
            if (!tip.isShowing()) {
                tip.setComponentLinked(jTextFieldNombre);
                tip.setMessage("Ya existe esa palabra");
                tip.setMode(Tooltip.ERROR);
                tip.setVisible(true);
            }
        } 
        catch (ClassNotFoundException ex) { } 
        catch (IOException ex) { } catch (Exception ex) {
            Logger.getLogger(JDialogFormNuevaPalabra.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButtonCrearActionPerformed

