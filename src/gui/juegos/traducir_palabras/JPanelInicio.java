    private void customInitComponents() {
        try {
            BDManagment bd = BDResolver.getInstance();
            Object idiomas[] = null;
            Object grupos[] = null;
            Object idiomasGrupos[] = null;
            ActionListener ALFlush = new OnActionFlushError();
            
            // Se llena el combo box con idiomas y grupos.
            idiomas = bd.idiomas.buscar().toArray();
            grupos = bd.grupos.buscar().toArray();
            
            // Se genera una array conjunta entre los dos resultados.
            idiomasGrupos = new Object[idiomas.length + grupos.length];
            
            for (int i = 0 ; i < idiomas.length ; i++)
                idiomasGrupos[i] = idiomas[i];
            
            for (int i = 0 ; i < grupos.length ; i++)
                idiomasGrupos[i + idiomas.length] = grupos[i];
            
            // Se establecen los resultados los combo box.
            jComboBoxIdiomaGrupo.setModel(new DefaultComboBoxModel(idiomasGrupos));
            jComboBoxIdioma.setModel(new DefaultComboBoxModel(idiomas));

            // Se añade un listener para borrar los errores mostrados, cuando hay alguna acción.
            jComboBoxIdioma.addActionListener(ALFlush);
            jComboBoxIdiomaGrupo.addActionListener(ALFlush);

        } catch (SQLException ex) {
            Logger.getLogger(JPanelInicio.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(JPanelInicio.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(JPanelInicio.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
    private class OnActionFlushError implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent e) {
            JLabelError.setText("");
        }
        
    }
