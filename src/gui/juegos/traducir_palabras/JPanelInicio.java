    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Object idiomaGrupoSelected = jComboBoxIdiomaGrupo.getSelectedItem();
        Object idiomaSelected = jComboBoxIdioma.getSelectedItem();
        PartidaJuegoTraducir partida;
        ArrayList<Palabra> palabras;
        BDManagment bd = null;

        if (idiomaGrupoSelected.equals(idiomaSelected))
            JLabelError.setText("Los idiomas no pueden ser los mismos.");
        else try {
            bd = BDResolver.getInstance();
            
            if (idiomaGrupoSelected instanceof Idioma)
                palabras = bd.palabras.buscarAleatoriamente((Idioma) idiomaGrupoSelected, (Idioma) idiomaSelected);
            else
                palabras = bd.palabras.buscarAleatoriamente((Grupo) idiomaGrupoSelected, (Idioma) idiomaSelected);
            
            if (palabras.size() < 10) 
                JLabelError.setText("Debe tener al menos 10 palabras traducibles.");
            else {
                partida = new PartidaJuegoTraducir(palabras, (Idioma) idiomaSelected);
                JuegosCtrl.getInstance().setPartida(partida);
                getGestorPaneles().setVisiblePanel(new JPanelTraducir(getGestorPaneles()), false);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(JPanelInicio.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(JPanelInicio.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(JPanelInicio.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed


