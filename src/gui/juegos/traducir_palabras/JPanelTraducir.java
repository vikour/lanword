    private void jButtonContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonContinuarActionPerformed
        partida.anyadirRespuesta(jTextFieldTraduccion.getText());
        palabra = partida.getPalabra();
        
        // Si la palabra es distinta a NULL, significa que sigue el juego.
        if (palabra != null) {
            jLabelPalabra.setText(palabra.toString());
            jTextFieldTraduccion.setText("");
        }
        else
            getGestorPaneles().setVisiblePanel(new JPanelPuntuacion(getGestorPaneles()), false);
    }//GEN-LAST:event_jButtonContinuarActionPerformed


